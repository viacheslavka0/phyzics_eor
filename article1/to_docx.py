def add_paragraph(text, bold=False, italic=False, alignment=None, font_size=14,
                  space_after=0, space_before=0, first_indent=True):
    p = doc.add_paragraph()
    if alignment is not None:
        p.alignment = alignment
    pf = p.paragraph_format
    pf.space_after = Pt(space_after)
    pf.space_before = Pt(space_before)
    pf.line_spacing = 1.5
    if not first_indent:
        pf.first_line_indent = Cm(0)
    else:
        pf.first_line_indent = Cm(1.25)
    run = p.add_run(text)
    run.font.name = "Times New Roman"
    run.font.size = Pt(font_size)
    run.bold = bold
    run.italic = italic
    rpr = run._element.get_or_add_rPr()
    rFonts = rpr.find(qn("w:rFonts"))
    if rFonts is None:
        from lxml.etree import SubElement
        rFonts = SubElement(rpr, qn("w:rFonts"))
    rFonts.set(qn("w:eastAsia"), "Times New Roman")
    rFonts.set(qn("w:cs"), "Times New Roman")
    return p


def add_mixed_paragraph(parts, alignment=None, first_indent=True, space_after=0, space_before=0):
    """parts = list of (text, bold, italic)"""
    p = doc.add_paragraph()
    if alignment is not None:
        p.alignment = alignment
    pf = p.paragraph_format
    pf.space_after = Pt(space_after)
    pf.space_before = Pt(space_before)
    pf.line_spacing = 1.5
    pf.first_line_indent = Cm(1.25) if first_indent else Cm(0)
    for text, bold, italic in parts:
        run = p.add_run(text)
        run.font.name = "Times New Roman"
        run.font.size = Pt(14)
        run.bold = bold
        run.italic = italic
        rpr = run._element.get_or_add_rPr()
        rFonts = rpr.find(qn("w:rFonts"))
        if rFonts is None:
            from lxml.etree import SubElement
            rFonts = SubElement(rpr, qn("w:rFonts"))
        rFonts.set(qn("w:eastAsia"), "Times New Roman")
        rFonts.set(qn("w:cs"), "Times New Roman")
    return p


